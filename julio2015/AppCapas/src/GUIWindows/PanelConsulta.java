    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
            try
            {
                ArrayList<Estudiante> listEst = ManejadorEstudiantesBL.Consultar();
                
                //Añade un item al ComboBox
                jComboBox1.addItem("Seleccione..");
                for (Estudiante estudiante : listEst) {
                    //Añade los items a partir del ArrayList al ComboBox
                    jComboBox1.addItem(estudiante.GetIdentificacion());
                }
            }
            catch(Exception ex)
            {
                    String msn = ex.getMessage();
            }
    }//GEN-LAST:event_jButton2ActionPerformed

