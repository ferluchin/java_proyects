    private void btnPresentarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPresentarActionPerformed
        try
        {
            //Recupera los datos 
            ArrayList<Object> listEst = ManejadorEstudiantesBL.ConsultarLista();
            String msn = "";
            
            
            for (Object object : listEst) {
                msn = msn + "\n" + object.toString();
            }
            
            System.out.println(msn);
            jTextArea1.setText(msn);

        } catch (ClassNotFoundException ex) {
            Logger.getLogger(PanelConsulta.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(PanelConsulta.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnPresentarActionPerformed

