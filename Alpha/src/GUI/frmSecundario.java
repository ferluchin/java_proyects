            //conec.GetConnection();

            if (conec != null) {
                JOptionPane.showMessageDialog(null, "Conexi√≥n Realizada Correctamente");
            }
            Persona ArrPersonas[] = ManejadorPersonas.Consultar();
            String strSentencia = null;
            int intArreglo = ArrPersonas.length;
            JOptionPane.showMessageDialog(rootPane, intArreglo);
            
