    private void btnIngresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIngresarActionPerformed

        lblResultado.setText("");

        try {
            Connection conexion = DriverManager.getConnection("jdbc:mysql://localhost/javapbd", "root", "");
            Statement comando = conexion.createStatement();
            comando.executeUpdate("insert into personas(nombres, apellidos, identificacion, edad) values ('" + txtNombres.getText() + "','" + txtApellidos.getText() + "'," + txtIdentificacion.getText() + "," + txtEdad.getText() + ");"
                    + "");
            conexion.close();
            lblResultado.setText("se registraron los datos");
            txtNombres.setText("");
            txtApellidos.setText("");
            txtIdentificacion.setText("");
            txtEdad.setText("");

        } catch (SQLException ex) {
            setTitle(ex.toString());
        }
    }//GEN-LAST:event_btnIngresarActionPerformed

